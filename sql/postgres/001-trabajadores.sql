create table if not exists cargos (
  id_cargo integer generated by default as identity primary key,
  nombre_cargo varchar(100) not null unique,
  descripcion varchar(250),
  activo boolean not null default true
);

create table if not exists areas (
  id_area integer generated by default as identity primary key,
  nombre_area varchar(100) not null unique,
  descripcion varchar(250),
  activo boolean not null default true
);

create table if not exists condiciones_laborales (
  id_condicion_laboral integer generated by default as identity primary key,
  nombre_condicion_laboral varchar(100) not null unique,
  descripcion varchar(250),
  activo boolean not null default true
);

create table if not exists trabajadores (
  id_trabajador integer generated by default as identity primary key,
  dni char(8),
  codigo_trabajador varchar(20),
  codigo_biometrico varchar(30),
  apellidos varchar(100) not null,
  nombres varchar(100) not null,
  id_cargo integer references cargos (id_cargo),
  id_area integer references areas (id_area),
  id_condicion_laboral integer references condiciones_laborales (id_condicion_laboral),
  fecha_ingreso date,
  activo boolean not null default true,
  correo varchar(150),
  telefono varchar(30),
  observacion varchar(500),
  fecha_registro timestamp(0) not null default current_timestamp
);

create unique index if not exists ux_trabajadores_codigo_trabajador
  on trabajadores (codigo_trabajador)
  where codigo_trabajador is not null;

create index if not exists ix_trabajadores_area on trabajadores (id_area, activo);
create index if not exists ix_trabajadores_cargo on trabajadores (id_cargo, activo);
create index if not exists ix_trabajadores_condicion_laboral on trabajadores (id_condicion_laboral, activo);

create table if not exists trabajador_biometrico (
  id_trabajador_biometrico integer generated by default as identity primary key,
  id_trabajador integer not null unique references trabajadores (id_trabajador),
  userid_biometrico integer not null unique,
  activo boolean not null default true,
  fecha_registro timestamp(0) not null default current_timestamp,
  observacion varchar(250)
);

